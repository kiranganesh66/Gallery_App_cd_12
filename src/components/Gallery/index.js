class Gallery extends Component {
  state = {activeId: imagesList[0].id}

  changingTheActiveID = id => {
    this.setState({activeId: id})
  }

  filterList = () => {
    const {activeId} = this.state
    const filterdetails = imagesList.filter(eachEl => eachEl.id === activeId)
    return filterdetails
  }

  render() {
    const {activeId} = this.state
    const fileterList = this.filterList()
    return (
      <div className="mainBG">
        <div className="image_bg">
          {fileterList.map(eachone => (
            <li className="list" key={eachone.id}>
              <img
                alt={eachone.imageAltText}
                src={eachone.imageUrl}
                className="imgSize"
              />
            </li>
          ))}
        </div>

        {/*   nate Below page */}

        <div>
          <h1 className="Heading">Nature Photography</h1>
          <p className="sign">Nature Photography by Rahul</p>
          <ul className="Below_ThumN_Contanier">
            {imagesList.map(eachOne => (
              <ThumbnailItem
                key={eachOne.id}
                eachOne={eachOne}
                changingTheActiveID={this.changingTheActiveID}
                isActive={activeId === eachOne.id}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
